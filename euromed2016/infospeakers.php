	<span class="title"> Info for Speakers </span>

	<span class="call"> Full Papers </span>

	<p style = "text-align: justify; margin-left: 220px; margin-right: 220px; margin-top: 160px;">Authors of accepted full papers will have to 		give a 25 minute oral presentation (including 5-10 minutes for questions from the audience).</p>
	<p style = "text-align: justify; margin-left: 220px; margin-right: 220px; margin-top: 0px;">Please present yourself to the Session Chair 		well before your session start.</p>
	<p style = "text-align: justify; margin-left: 220px; margin-right: 220px; margin-top: 0px;">Authors slides will be presented via a video 		projector (see details below).</p>

	<span class="call"> Short/Project Papers </span>

	<p style = "text-align: justify; margin-left: 220px; margin-right: 220px; margin-top: 160px;">Authors of accepted SHORT and PROJECT papers 		will be required to do a short presentation (15 minutes net time). Project Paper presentations will have a duration of 15 minutes long. Short 		Paper presentations will have a duration of 10 minutes long. Moreover, there will be an opportunity for informal questions and discussion 		during the dedicated Short Paper posters breaks. Each <b>SHORT paper</b> will be also presented in a <b>poster session</b> (during the coffee 		break following oral presentation). It is therefore a REQUIREMENT for publication that you prepare both a short oral 	presentation and a 		poster of your <b> short paper</b> for display. The exact lay out of the poster is at your discretion but the 	poster should be of A0 size 		and preferably laminated (Guidelines). If you have any queries regarding these final requirements,feel free to <a href="#"style="color:blue"> 		contact us </a>.</p>

	<p><a href="#" style="color:blue; margin-left: 220px;">Instructions for poster presentations</a></p>

